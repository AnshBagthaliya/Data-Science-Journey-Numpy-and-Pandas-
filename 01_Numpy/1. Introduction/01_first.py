import numpy as np
import pandas as pd

# Create a NumPy array
arr = np.array([10, 20, 30, 40, 50])

# Convert NumPy array to Pandas Series
series = pd.Series(arr)

# Display both
print("NumPy Array:")
print(arr)

print("\nPandas Series:")
print(series)


